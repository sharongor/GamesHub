package il.movies.application.models;

public class GamesArray {
    private int id;
    private String nameGame;

    public GamesArray(int id, String nameGame) {
        this.id = id;
        this.nameGame = nameGame;
    }

    public int getId() {
        return id;
    }

    public String getNameGame() {
        return nameGame;
    }
}


